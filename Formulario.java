    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if (!txtNombre.getText().isEmpty()&&!txtEdad.getText().isEmpty()
                &&!txtCargo.getText().isEmpty()) {
            funcionario = new Funcionario();
            funcionario.setNombre(txtNombre.getText());
            funcionario.setEdad(Integer.parseInt(txtEdad.getText()));
            funcionario.setCargo(txtCargo.getText());
            
            funcionarioServi.createFUncionario(funcionario);
            lblMensaje.setText(Constantes.MENSAJE);
limpiar();
            
            
        } else {
            lblMensaje.setText("CAMPOS VACIOS¡¡");
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        funcionario = funcionarioServi.getFuncionario(Integer.parseInt(txtId.getText()));

        if (funcionario !=null) {
            txtNombre.setText(funcionario.getNombre());
            txtEdad.setText(String.valueOf(funcionario.getEdad()));
            txtCargo.setText(funcionario.getCargo());
        }
        lblMensaje.setText(Constantes.MENSAJE);
    }//GEN-LAST:event_btnConsultarActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        funcionario = funcionarioServi.getFuncionario(Integer.parseInt(txtId.getText()));
        
        if (funcionario!=null) {
         
        
            funcionario.setNombre(txtNombre.getText());
            funcionario.setEdad(Integer.parseInt(txtEdad.getText()));
            funcionario.setCargo(txtCargo.getText());
            
            funcionarioServi.updateFuncionario(funcionario);
            limpiar();
        }
            lblMensaje.setText(Constantes.MENSAJE);
}
            public void limpiar (){

txtId.setText("");
txtCargo.setText("");
txtEdad.setText("");
txtNombre.setText("");


    }//GEN-LAST:event_btnActualizarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if (funcionario !=null) {
            funcionarioServi.deleteFuncionario(Integer.parseInt(txtId.getText()));
            limpiar();
        }
          lblMensaje.setText(Constantes.MENSAJE);
    }//GEN-LAST:event_btnEliminarActionPerformed

